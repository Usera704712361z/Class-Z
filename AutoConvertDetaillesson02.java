public class AutoConvertDetaillesson02 {
	public static void main(String [] args) {
		int n1 = 10;
		double d1 = n1 + 1.1;
		double n2 = 1.1;
		byte b4 = 1;
		short s3 = 100;
		int num200 = 1;
		float num300 = 1.1F;
		double num500 = b4 + s3 + num200 + num300;
		System.out.println(n1);
		System.out.println(d1);
		System.out.println(n2);
		System.out.println(num500);
	}
}